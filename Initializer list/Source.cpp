//////////////////////////////////////////////////////////
#include <iostream>
#include <initializer_list>
#include <vector>
//////////////////////////////////////////////////////////
using namespace std;
//////////////////////////////////////////////////////////
class Test
{
    vector<int> vec;
public:
    Test(initializer_list<int> list) : vec(list)
    {}
    
    void print()
    {
        for(auto i : vec)
        {
            cout<< i <<", ";
        }
    }
};
////////////////////////////////////////////////////////
int main() {
    Test test{1, 2, 3};
    test.print();

    return 0;
}
